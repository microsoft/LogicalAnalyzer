namespace Microsoft.CST.OAT
{
    /// <summary>
    /// The boolean operators supported by the Analyzer.
    /// </summary>
    public enum BoolOperator
    {
        /// <summary>
        /// Logical and
        /// </summary>
        AND,
        /// <summary>
        /// Logical or
        /// </summary>
        OR,
        /// <summary>
        /// Logical xor
        /// </summary>
        XOR,
        /// <summary>
        /// Logical not and
        /// </summary>
        NAND,
        /// <summary>
        /// Logical not or
        /// </summary>
        NOR,
        /// <summary>
        /// Logical not
        /// </summary>
        NOT
    }
}
